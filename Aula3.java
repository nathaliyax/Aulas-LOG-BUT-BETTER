package nljava;

import java.util.Scanner;

public class aula6_2 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		
		int key = sc.nextInt();
		
		switch (key) {
		case 1:
			System.out.println("1");
			break;
		case 2:
			System.out.println("2");
			break;
			default:
				System.out.println("erro");
				
		}
 
	}

}
